func TestEngineDetect(t *testing.T) {
	eng, err := NewEngine()
	if err != nil {
		t.Fatalf("NewEngine failed: %v", err)
	}
	defer eng.Close()

	t.Logf("supported protocols: %d", GetNumSupported())

	tests := []struct {
		name     string
		rawPkt   []byte
		key      FlowKey
		wantName string // expected protocol name or empty to skip check
	}{
		{
			name: "DNS query",
			rawPkt: []byte{
				0x45, 0x00, 0x00, 0x3c, 0x00, 0x01, 0x00, 0x00, 0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x64, 0x08, 0x08, 0x08, 0x08,
				0xf8, 0x2a, 0x00, 0x35, 0x00, 0x28, 0x00, 0x00,
				0x12, 0x34, 0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x06, 0x67, 0x6f, 0x6f, 0x67, 0x6c, 0x65, 0x03, 0x63, 0x6f, 0x6d, 0x00,
				0x00, 0x01, 0x00, 0x01,
			},
			key: FlowKey{
				SrcIP: "192.168.1.100", DstIP: "8.8.8.8",
				SrcPort: 63530, DstPort: 53, Protocol: 17,
			},
			wantName: "DNS",
		},
		{
			name: "HTTP GET request",
			rawPkt: []byte{
				0x45, 0x00, 0x00, 0x50, 0x00, 0x01, 0x00, 0x00, 0x40, 0x06, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x64, 0x5d, 0xb8, 0xd8, 0x22,
				0xc2, 0x70, 0x00, 0x50, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x50, 0x02, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00,
				0x47, 0x45, 0x54, 0x20, 0x2f, 0x20, 0x48, 0x54, 0x54, 0x50, 0x2f, 0x31, 0x2e, 0x31, 0x0d, 0x0a,
				0x48, 0x6f, 0x73, 0x74, 0x3a, 0x20, 0x77, 0x77, 0x77, 0x2e, 0x67, 0x6f, 0x6f, 0x67, 0x6c, 0x65,
				0x2e, 0x63, 0x6f, 0x6d, 0x0d, 0x0a, 0x0d, 0x0a,
			},
			key: FlowKey{
				SrcIP: "192.168.1.100", DstIP: "93.184.216.34",
				SrcPort: 49776, DstPort: 80, Protocol: 6,
			},
			wantName: "HTTP",
		},
		{
			name: "SSH banner",
			rawPkt: []byte{
				0x45, 0x00, 0x00, 0x30, 0x00, 0x01, 0x00, 0x00, 0x40, 0x06, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x64, 0xc0, 0xa8, 0x01, 0x01,
				0x00, 0x16, 0xc6, 0x68, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x50, 0x02, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00,
				0x53, 0x53, 0x48, 0x2d, 0x32, 0x2e, 0x30, 0x2d, 0x4f, 0x70, 0x65, 0x6e, 0x53, 0x53, 0x48, 0x5f,
			},
			key: FlowKey{
				SrcIP: "192.168.1.100", DstIP: "192.168.1.1",
				SrcPort: 22, DstPort: 50832, Protocol: 6,
			},
			wantName: "SSH",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result, err := eng.Detect(tt.rawPkt, tt.key)
			if err != nil {
				t.Fatalf("Detect failed: %v", err)
			}
			t.Logf("Protocol: %s (master=%d, app=%d, category=%s)",
				result.ProtoName, result.MasterID, result.AppID, result.Category)
			if tt.wantName != "" && result.ProtoName != tt.wantName {
				// Don't fail hard — some protocols need multiple packets to classify
				t.Logf("NOTE: expected %q, got %q (may need more packets)", tt.wantName, result.ProtoName)
			}
		})
	}
}

func TestEngineIdleCleanup(t *testing.T) {
	eng, err := NewEngine()
	if err != nil {
		t.Fatalf("NewEngine failed: %v", err)
	}
	defer eng.Close()

	pkt := []byte{
		0x45, 0x00, 0x00, 0x30, 0x00, 0x01, 0x00, 0x00, 0x40, 0x06, 0x00, 0x00,
		0xc0, 0xa8, 0x01, 0x64, 0xc0, 0xa8, 0x01, 0x01,
		0x00, 0x16, 0xc6, 0x68, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x50, 0x02, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00,
		0x53, 0x53, 0x48, 0x2d, 0x32, 0x2e, 0x30,
	}

	key := FlowKey{
		SrcIP: "10.0.0.1", DstIP: "10.0.0.2",
		SrcPort: 9999, DstPort: 22, Protocol: 6,
	}

	if _, err := eng.Detect(pkt, key); err != nil {
		t.Fatalf("Detect failed: %v", err)
	}
	if eng.FlowCount() != 1 {
		t.Fatalf("expected 1 flow, got %d", eng.FlowCount())
	}

	// Immediate cleanup with 0 timeout should remove all
	removed := eng.IdleCleanup(0)
	if removed != 1 {
		t.Fatalf("expected 1 removed, got %d", removed)
	}
	if eng.FlowCount() != 0 {
		t.Fatalf("expected 0 flows after cleanup, got %d", eng.FlowCount())
	}
}
